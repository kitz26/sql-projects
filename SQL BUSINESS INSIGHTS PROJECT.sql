-- BASIC LEVEL
-- 1 FIND TOTAL SALES. 
SELECT SUM(SALESAMOUNT) AS TOTAL_SALES FROM ORDERS;

-- 2 FIND TOTAL ORDERS PER CITY
SELECT CITY,COUNT(ORDERID) AS TOTAL_ORDERS FROM ORDERS
GROUP BY CITY;

-- 3 FIND AVERAGE  SALES PER CATEGORY
SELECT CATEGORY,AVG(SALESAMOUNT) AS AVG_SALES FROM ORDERS
GROUP BY CATEGORY;

-- 4 FIND TOP 3 HIGHEST SALES ORDERS
SELECT SALESAMOUNT  FROM ORDERS
ORDER BY SALESAMOUNT DESC
LIMIT 3;

-- INTERMIEDIATE LEVEL 
-- 5 FIND CITIES WITH TOTAL SALES >30000
SELECT CITY ,SUM(SALESAMOUNT)  FROM ORDERS
GROUP BY CITY
HAVING SUM(SALESAMOUNT)>30000;

-- 6 FIND CATEGORIES WITH AVG UNITSSOLD > AVG OVERALL AVG
SELECT CATEGORY,AVG(UNITSSOLD) FROM ORDERS
GROUP BY CATEGORY
HAVING AVG(UNITSSOLD)>(SELECT AVG(SALESAMOUNT)FROM ORDERS);

-- 7 FIND CUSTOMER WITH MORE THAN ON ORDER
SELECT CUSTOMERID,COUNT(*) FROM ORDERS
GROUP BY CUSTOMERID
HAVING COUNT(*)>1;

-- 8 FIND CATEGORY WITH HIGHEST REVENUE
SELECT CATEGORY ,SUM(SALESAMOUNT) AS TOTAL_REVENUE FROM ORDERS
GROUP BY CATEGORY
ORDER BY TOTAL_REVENUE DESC
LIMIT 1;

-- ADVANCED LEVEL(SUBQUERIES)
-- 9 FIND ORDERS WHERE SALESAMOUNT> AVERAGE SALESAMOUNT
SELECT * FROM ORDERS
WHERE SALESAMOUNT>(SELECT AVG(SALESAMOUNT) FROM ORDERS);

-- 10 FIND PRODUCTS WHOSE TOTAL SALES > AVERAGE  PRODUCT TOTAL SALES
SELECT PRODUCTID,SUM(SALESAMOUNT) AS TOTAL_SALES  FROM ORDERS
GROUP BY PRODUCTID
HAVING SUM(SALESAMOUNT)>(SELECT AVG(SALESAMOUNT) FROM ORDERS);

-- 11 FIND CITIES WHERE TOTAL SALES > DELHI'S TOTAL SALES
SELECT CITY, SUM(SALESAMOUNT) FROM ORDERS
GROUP BY CITY
HAVING SUM(SALESAMOUNT)>(SELECT SUM(SALESAMOUNT) FROM ORDERS
WHERE CITY='DELHI');

