-- WINDOW FUNCTION
-- 1 RANK ORDERS BASED ON SALESAMOUNT
SELECT *,
RANK() OVER(ORDER BY SALESAMOUNT) FROM ORDERS;

-- 2 FIND TOP 2 SALES IN EACH CATEGORY
SELECT *
FROM (
    SELECT *,
           ROW_NUMBER() OVER (PARTITION BY CATEGORY ORDER BY SALESAMOUNT DESC) AS rn
    FROM ORDERS
) t
WHERE rn <= 2;

